// Element Creater================
const addCards = function (box, Foods) {
    for (food of Foods) {
        const cardSmall = document.createElement('div');
        const foodImg = document.createElement('img');
        const cardText = document.createElement('div');
        const foodNameHTML = document.createElement('p');
        const foodNameThai = document.createElement('span');
        const foodPrice = document.createElement('span');
        const ingredientHTML = document.createElement('div');
        const foodDescription = document.createElement('div');
        const preservationHTML = document.createElement('div');

        cardSmall.classList.add("card-small", "flex-shrink-0", "mx-2");
        cardSmall.setAttribute("data-bs-toggle", "modal");
        cardSmall.setAttribute("data-bs-target", "#modal-bakery");
        foodImg.classList.add("d-block", "card-img-small");
        foodImg.setAttribute("src", food.imgPath);
        cardText.classList.add("card-text", "w-100", "h-auto", "pt-1", "ps-1");

        if (box == appetizerBox || box == mainCourseBox) {
            foodNameHTML.classList.add("card-name-small", "mb-md-1");
            foodNameThai.classList.add("card-name-thai-small", "text-wrap", "float-start");
            foodPrice.classList.add("card-price-small", "float-end");
        } else {
            foodNameHTML.classList.add("card-name-small-white", "mb-md-1");
            foodNameThai.classList.add("card-name-thai-small-white", "text-wrap", "float-start");
            foodPrice.classList.add("card-price-small-white", "float-end");
        }
        foodNameHTML.innerHTML = food.nameHTML;
        foodNameThai.innerText = food.nameThai;
        foodPrice.innerText = food.price + ",-";
        ingredientHTML.classList.add("ingredient", "d-none");
        ingredientHTML.innerHTML = food.ingredientHTML;
        foodDescription.classList.add("productDescription", "d-none");
        foodDescription.innerText = food.description;
        preservationHTML.classList.add("preservation", "d-none");
        preservationHTML.innerHTML = food.preservationHTML;

        cardText.appendChild(foodNameHTML);
        cardText.appendChild(foodNameThai);
        cardText.appendChild(foodPrice);
        cardSmall.appendChild(foodImg);
        cardSmall.appendChild(cardText);
        cardSmall.appendChild(ingredientHTML);
        cardSmall.appendChild(foodDescription);
        cardSmall.appendChild(preservationHTML);

        box.appendChild(cardSmall);
    }
};

const appetizerBox = document.querySelector('#appetizerBox');
const mainCourseBox = document.querySelector('#mainCourseBox');
const westernBox = document.querySelector('#westernBox');
const saladBox = document.querySelector('#saladBox');

addCards(appetizerBox, Appetizers);
addCards(mainCourseBox, MainCourses);
addCards(westernBox, Westerns);
addCards(saladBox, Salads);